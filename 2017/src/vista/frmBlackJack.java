    private void btnnaipe1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnaipe1ActionPerformed
        // TODO add your handling code here:
        bloquearB();
        btnnaipe1.setIcon(bj.obtenerCartaA());
        lblsumaA.setText(String.valueOf(bj.mostrarsumaA()));
        if (bj.mostrarsumaA() == 21) {
            lblganador.setText("A");
            bloquearTodo();
        }
        if (bj.mostrarsumaA() > 21) {
            bloquearA();
            lblganador.setText("B");           
        }
    }//GEN-LAST:event_btnnaipe1ActionPerformed

