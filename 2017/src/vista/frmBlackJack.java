    public void bloquearA(){
        btnnaipe1.setEnabled(false);
        btnplantarA.setEnabled(false);
        btnwin.setEnabled(false);
    }
    public void bloquearB(){
        btnnaipe2.setEnabled(false);
        btnplantarB.setEnabled(false);
        btnwin.setEnabled(false);
    }
    public void plantarA(){
        btnwin.setEnabled(true);
        btnnaipe1.setEnabled(false);
        btnnaipe2.setEnabled(true);
        btnplantarB.setEnabled(true);
    }
    public void plantarB(){
        btnwin.setEnabled(true);
        btnnaipe1.setEnabled(true);
        btnplantarA.setEnabled(true);
        btnnaipe2.setEnabled(false);
    }
    public void bloquearTodo(){
        btnnaipe1.setEnabled(false);
        btnplantarA.setEnabled(false);
        btnwin.setEnabled(false);
        btnnaipe2.setEnabled(false);
        btnplantarB.setEnabled(false);
    }
    public void limpiar() {
        btnnaipe1.setIcon(bj.obtenerIconoReverse());
        btnnaipe2.setIcon(bj.obtenerIconoReverse());
        lblganador.setText("");
        lblsumaA.setText("");
        lblsumaB.setText("");
        lblsumaA.setEnabled(true);
        lblsumaB.setEnabled(true);
        btnnaipe1.setEnabled(true);
        btnnaipe2.setEnabled(true);
        btnplantarA.setEnabled(true);
        btnplantarB.setEnabled(true);
        bj.reiniciar();
        btnwin.setEnabled(true);
    }

